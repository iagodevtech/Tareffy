-- Criar buckets de storage necessários para o Tareffy
-- Execute este script no Supabase para criar os buckets de armazenamento

-- Criar bucket para avatars
INSERT INTO storage.buckets (id, name, public, file_size_limit, allowed_mime_types)
VALUES (
  'avatars',
